ORG 100H
.DATA
LOWER DW 5DUP(0)     
UPPER DW 5DUP(0)
NUMERIC DW 5DUP(0)
REST DW 5DUP(0)    
COUNT_L DW 0
COUNT_U DW 0
COUNT_N DW 0
COUNT_R DW 0

.CODE
XOR AX,AX
XOR BX,BX
XOR CX,CX
XOR DX,DX
XOR SI,SI

INPUT:
MOV AH,1
INT 21H
MOV AH,0
CMP AX,8
JE EXIT

CMP AX,57
JG UPPER1
CMP AX,48
JL CHAR1
MOV SI,COUNT_N
MOV [NUMERIC+SI],AX
INC COUNT_N
JMP INPUT

UPPER1:
CMP AX,65
JL CHAR1
CMP AX,90
JG LOWER1
MOV SI,COUNT_U
MOV [UPPER+SI],AX
INC COUNT_U
JMP INPUT

LOWER1:
CMP AX,122
JG CHAR1
MOV SI,COUNT_L
MOV [LOWER+SI],AX
INC COUNT_L
JMP INPUT

CHAR1:
MOV SI,COUNT_R
MOV [REST+SI],AX
INC COUNT_R
JMP INPUT

EXIT:
RET

